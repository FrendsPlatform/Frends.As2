namespace Frends.As2.ValidateAndParsePayload.Definitions;

/// <summary>
/// Result of the task.
/// </summary>
public class Result
{
    /// <summary>
    /// Indicates if the task completed successfully.
    /// </summary>
    /// <example>true</example>
    public bool Success { get; set; }

    /// <summary>
    /// Error that occurred during task execution.
    /// </summary>
    /// <example>object { string Message, object { Exception Exception } AdditionalInfo }</example>
    public Error Error { get; set; }

    /// <summary>
    /// Decrypted/decompressed business data (EDI/XML/JSON).
    /// </summary>
    /// <example>Test message</example>
    public string Payload { get; set; }

    /// <summary>
    /// AS2 sender identifier.
    /// </summary>
    /// <example>PartnerAS2</example>
    public string As2From { get; set; }

    /// <summary>
    /// AS2 receiver identifier.
    /// </summary>
    /// <example>MyCompanyAS2</example>
    public string As2To { get; set; }

    /// <summary>
    /// ID of the received message.
    /// </summary>
    /// <example>"&lt;123456789@example.com&gt;"</example>
    public string MessageId { get; set; }

    /// <summary>
    /// MDN receipt generated by the AS2Receiver. To be used later in CreateMdnResponse.
    /// </summary>
    /// <example>object { "Headers": "...", "Message": "...", "Content": "..." }</example>
    public MdnData MdnReceipt { get; set; }
}
